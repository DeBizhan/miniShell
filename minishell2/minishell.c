#include "minishell.h"
#include <unistd.h>
#include <stdio.h>
#include <stdlib.h>
#include <readline/readline.h>
#include <readline/history.h>

int main()
{
    char *input;

    while(1)
    {
        input = readline("minishell: ");
        add_history(input);

        if (strcmp(input, "exit") == 0)
            exit(0);
        line_read(input);
        free(input);
    }
}
